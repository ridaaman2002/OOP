public class car {
	//Atributs 
	public String brand;
	public String model;
	public int amountOfFuel;
	 
	//constucteur 
	public car() {
		brand="";
		model="";
		amountOfFuel=0;
		
	}
	public car(String brandX, String modelX, int amountX) {
		brand=brandX;
		model=modelX;
		amountOfFuel=amountX;
	}
	
}
